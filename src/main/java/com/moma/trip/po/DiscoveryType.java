package com.moma.trip.po;

public class DiscoveryType {

	private String discoveryTypeId;
	private String name;
	private String type;
	private Integer sort;

	public String getDiscoveryTypeId() {
		return discoveryTypeId;
	}

	public void setDiscoveryTypeId(String discoveryTypeId) {
		this.discoveryTypeId = discoveryTypeId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getSort() {
		return sort;
	}

	public void setSort(Integer sort) {
		this.sort = sort;
	}

}
