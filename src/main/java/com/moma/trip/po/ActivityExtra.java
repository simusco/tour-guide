package com.moma.trip.po;

public class ActivityExtra {

	private String activityExtraId;
	private String activityPlanId;
	private String type;
	private String content;

	public String getActivityExtraId() {
		return activityExtraId;
	}

	public void setActivityExtraId(String activityExtraId) {
		this.activityExtraId = activityExtraId;
	}

	public String getActivityPlanId() {
		return activityPlanId;
	}

	public void setActivityPlanId(String activityPlanId) {
		this.activityPlanId = activityPlanId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
